package pickapath;

public class Arrow {
		private String text;
		private int x;
		private int y;
		private int length;
		private int height;
		
		public Arrow(int x,int y,int length,int height, String text) {
			this.x = x;
			this.y = y;
			this.length = length;
			this.height = height;
			this.text = text;
}
}
